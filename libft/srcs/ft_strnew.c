#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;
	size_t	i;

	i = 0;
	str = (char *)malloc(sizeof(char) * (size + 1));
	if (!str)
		return (NULL);
	while (size + 1 > 0)
	{
		str[i] = '\0';
		i++;
		size--;
	}
	return (str);
}
