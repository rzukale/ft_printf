#include "libft.h"

size_t	ft_int_length(int n)
{
	size_t	i;

	i = 0;
	if (n == 0)
		i++;
	else if (n < 0)
		i++;
	while (n)
	{
		n /= 10;
		i++;
	}
	return (i);
}
